# getting two number
a = 15
b = 30

# equal operator
print(a == b)
print(a == 15)

# Not equal operator
print(a != b)
print(a != 15)

# Grater than operator
print(a > b)
print(a > 15)

# less than operator
print(a < b)
print(a < 15)

# Grater than or equal operator
print(a >= b)
print(a >= 15)

# Less than or equal operator
print(a <= b)
print(a <= 15)

